const glyphFor = (ch) => FONT[ch] ?? FONT[ch.toUpperCase()] ?? null;

export const supports = (ch) => glyphFor(ch) !== null;

// Turns text into {x, y} pixels, one blank column between glyphs.
export const buildPixels = (text) => {
  const pixels = [];
  let cursor = 0;
  for (const ch of text) {
    const glyph = glyphFor(ch);
    if (!glyph) throw new Error(`지원하지 않는 문자입니다: "${ch}"`);
    const width = glyph[0].length;
    glyph.forEach((row, y) => {
      [...row].forEach((cell, dx) => {
        if (cell === "#") pixels.push({ x: cursor + dx, y });
      });
    });
    cursor += width + 1;
  }
  return { pixels, width: Math.max(cursor - 1, 0) };
};
